// Step 1: Initialize an array with several numbers
let numbers = [10, 20, 30, 40, 50];

// Step 2: Declare a variable to hold the sum
let sum = 0;

// Step 3: Use a for loop to iterate through the array
for (let i = 0; i < numbers.length; i++) {
    // Step 4: Sum up all the elements
    sum += numbers[i];
}

// Step 5: Print the final sum
console.log("The sum of all elements is:", sum);
